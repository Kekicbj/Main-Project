package OperatorPoredjenja;

public class OperatorPoredjenja {
    public static void main(String[] args) {
        int value1 = 1;
        int value2 = 2;
        System.out.println(value1 == value2);
        // nije jednako zato je false

        int value3 = 1;
        int value4 = 2;
        boolean isEqual = value3 == value4;
        System.out.println(isEqual);
        // jos jedan nacin da se ispise kod

        int value5 = 1;
        int value6 = 2;
        System.out.println(value5 != value6);
        // kada se ispituje da nije jednako
        // ista stvar je i za ostale operatore poredjenja

    }
}
