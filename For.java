public class For {
    public static void main(String[] args) {
        for ( int i = 0; i <5; i++) {
            System.out.println("Hello World");
        }

        for (int i = 0; i < 5; i+=2) {
            System.out.println("World");
            
        }
        for (int i = 5; i > 0 ; i--) {
            System.out.println("Hello");
        }
    }
}
